"""Language policies for Saguaro's static math extraction."""

from __future__ import annotations

from dataclasses import dataclass, field


@dataclass(frozen=True, slots=True)
class LanguagePolicy:
    """Math extraction policy for one language family."""

    language: str
    line_comment_markers: tuple[str, ...]
    block_comment_pairs: tuple[tuple[str, str], ...] = ()
    statement_terminators: tuple[str, ...] = ()
    assignment_tokens: tuple[str, ...] = ("=",)
    compound_assignment_tokens: tuple[str, ...] = ("+=", "-=", "*=", "/=", "%=")
    return_keywords: tuple[str, ...] = ("return",)
    loop_keywords: tuple[str, ...] = ("for", "while")
    newline_statements: bool = True
    ignore_prefixes: tuple[str, ...] = ()
    documentation_markers: tuple[str, ...] = ()
    math_function_names: tuple[str, ...] = field(
        default_factory=lambda: (
            "sum",
            "dot",
            "matmul",
            "sqrt",
            "exp",
            "log",
            "sin",
            "cos",
            "norm",
        )
    )


