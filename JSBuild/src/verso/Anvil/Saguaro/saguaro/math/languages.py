def policy_for(language: str) -> LanguagePolicy:
    """Return the math extraction policy for one language."""

    normalized = str(language or "").strip().lower()
    normalized = _ALIASES.get(normalized, normalized)
    return _POLICIES.get(normalized, _POLICIES["python"])


def supported_languages() -> set[str]:
    """Return the set of languages with explicit math policies."""

    return set(_POLICIES) | set(_ALIASES)
